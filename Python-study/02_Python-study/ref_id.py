# Pythonにおける変数は、変数の中身があるアドレスを扱っている（変数の中身自体は別のオブジェクトに属している）
# そのため、別の変数でも中身の数値が同じなら同じオブジェクトを指していることがある

num1 = 10
num2 = num1
print(id(num1))  # id()は変数が指しているオブジェクトの識別地を返す
print(id(num2))
num3 = 10
print(id(num3))

#
# ここまでの実行結果
#
# 11755976
# 11755976
# 11755976
#

# 以下では数字を変えてみる
num4 = 257
num5 = num4
num6 = 257
print(id(num4))
print(id(num5))
print(id(num6))

