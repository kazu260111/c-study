# 同じプログラムを実行しても数字が10のときは毎回全く同じアドレスを使用するのに対して、257のときは毎回全く違うアドレスを返す。
# Pythonの仕様として、-5~256まではよく使う数字として予めアドレスを用意していて、この範囲の数字を使うときは用意したメモリアドレスを使用している（変数に用意しておいたアドレスを渡している）
# そのため、使用する数字が特定範囲内の特定値なら使用するアドレスは毎回同じで、範囲外なら毎回ランダムになるのだと考えられる。(特定範囲内の数字はLinuxのASLRの対象外のため毎回固定のアドレスらしい)

