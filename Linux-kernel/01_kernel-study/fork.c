/* 
 * メモ：
 * fork関数ではコピーオンライト方式のおかげで毎回メモリをすべてコピーする必要がない（共有部分を必要なときだけ物理メモリにコピーする）のでメモリ使用量も少なく、コピーも高速に行える。
 * プログラムの作成当初は配列ではなく一つの変数を変えることでページフォールトが起きたか確認しようとしたが、変数を一つ変えるだけだとページフォールトの回数に変化がなかった。そこで巨大な配列に書き込むことで確実に複数のページに書き込み、多くのページフォールトを起こすことにした。
 */
	
